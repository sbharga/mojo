// A valid, no-op Wasm module whose function contains an i8x16.splat. Engines
// without fixed-width SIMD reject it during validation without downloading the
// larger SIMD engine artifact.
const SIMD_PROBE = new Uint8Array([
  0x00, 0x61, 0x73, 0x6d, 0x01, 0x00, 0x00, 0x00,
  0x01, 0x04, 0x01, 0x60, 0x00, 0x00,
  0x03, 0x02, 0x01, 0x00,
  0x0a, 0x09, 0x01, 0x07, 0x00, 0x41, 0x00, 0xfd, 0x0f, 0x1a, 0x0b,
]);

export function supportsWasmSimd() {
  return WebAssembly.validate(SIMD_PROBE);
}
